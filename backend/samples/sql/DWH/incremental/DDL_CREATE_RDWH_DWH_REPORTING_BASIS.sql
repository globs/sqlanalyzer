DROP TABLE DWH_REPORTING_BASIS IF EXISTS;

CREATE TABLE DWH_REPORTING_BASIS
(
    ID           SMALLINT     NOT NULL,
    CODE         VARCHAR(32)  NOT NULL,
    NAME         VARCHAR(64)  NOT NULL,
    CREATED_DATE TIMESTAMP(6) DEFAULT CURRENT TIMESTAMP,
    CONSTRAINT PK_DWH_REPORTING_BASIS
    PRIMARY KEY (ID)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;