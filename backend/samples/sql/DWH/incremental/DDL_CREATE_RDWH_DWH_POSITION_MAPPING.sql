DROP TABLE DWH_POSITION_MAPPING IF EXISTS;

CREATE TABLE DWH_POSITION_MAPPING
(
    POSITION_ID         INTEGER      NOT NULL,
    MAPPING_POSITION_ID INTEGER      NOT NULL,
    VALID_FROM          TIMESTAMP(6) NOT NULL,
    VALID_TO            TIMESTAMP(6) NOT NULL,
    MATH_OPERATOR       CHARACTER(1),
    CONSTRAINT PK_DWH_POSITION_MAPPING
    PRIMARY KEY (POSITION_ID,MAPPING_POSITION_ID,VALID_FROM)
) 
   ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;