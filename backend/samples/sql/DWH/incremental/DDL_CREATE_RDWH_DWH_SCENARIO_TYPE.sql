DROP TABLE DWH_SCENARIO_TYPE IF EXISTS;

CREATE TABLE DWH_SCENARIO_TYPE
(
    ID         INTEGER      NOT NULL,
    CODE       VARCHAR(32)  NOT NULL,
    NAME       VARCHAR(100) NOT NULL,
    VALID_FROM TIMESTAMP(6) NOT NULL,
    VALID_TO   TIMESTAMP(6) NOT NULL,
    CONSTRAINT PK_DWH_SCENARIO_TYPE
    PRIMARY KEY (ID)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;