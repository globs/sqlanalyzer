DROP TABLE TCSM_REF_TRANSITION IF EXISTS;
CREATE TABLE TCSM_REF_TRANSITION (
		  NORM VARCHAR(7)	NOT NULL, 
		  SAS_ENTITY VARCHAR(36)	NOT NULL, 
		  CTR_NF VARCHAR(9)	NOT NULL, 
		  SEC_NF SMALLINT	NOT NULL, 
		  UWY_NF SMALLINT	NOT NULL, 
		  CEDED_FLAG CHAR(1)	NOT NULL, 
		  INSURANCE_CONTRACT_GROUP VARCHAR(36)	NOT NULL, 
		  REPORTING_DATE VARCHAR(10)	NOT NULL, 
		  INTERVAL VARCHAR(10)	NOT NULL, 
		  TRANSITION_ANNUAL VARCHAR(4) , 
		  TRANSITION_FLG CHAR(1)	NOT NULL, 
		  TRANSITION_LOCKINDATE VARCHAR(10)	NOT NULL, 
		  TRANSITION_INPUT_CSM_AMT DECFLOAT(34) , 
		  DIRECT_TRANSITION_ACQ_COSTS_AMT DECFLOAT(34) , 
		  "USER_ID" VARCHAR(7)	NOT NULL,
	CONSTRAINT PK_TCSM_REF_TRANSITION 
	PRIMARY KEY (NORM, SAS_ENTITY, INSURANCE_CONTRACT_GROUP, REPORTING_DATE, INTERVAL, TRANSITION_FLG, TRANSITION_LOCKINDATE)) 
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;