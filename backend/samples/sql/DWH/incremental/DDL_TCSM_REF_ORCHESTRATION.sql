DROP TABLE TCSM_REF_ORCHESTRATION IF EXISTS;

CREATE TABLE TCSM_REF_ORCHESTRATION (
		CSM_IDENTIFIER_ID SMALLINT,
		SAS_ENTITY VARCHAR(36),
		UOA_CURRENCY VARCHAR(3),
		REPORTING_BASIS_ID VARCHAR(32),
		CSM_AMORTIZATION_PATTERN VARCHAR(15),
		ACCOUNTING_PERIOD VARCHAR(10),
		INITIAL_RUN_FLG SMALLINT,
		MAX_CLOSING_DATE DATE,
		SCENARIO_TYPE VARCHAR(15)
	)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;
