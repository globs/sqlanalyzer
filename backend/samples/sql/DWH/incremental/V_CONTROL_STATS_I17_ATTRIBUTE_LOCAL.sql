SELECT
	SSD_CF,
	ESB_CF,
	'I17_ATTRIBUTE_RETRO_PROFITABILITY' AS STAT_NAME,
	COUNT(DISTINCT RETCTR_NF) AS STAT_VALUE,
	BALSHRMTH_NF,
	BALSHEY_NF
FROM
