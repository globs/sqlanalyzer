DROP TABLE TCSM_QUOTES IF EXISTS;
CREATE TABLE TCSM_QUOTES  (
		  ROW_ID SMALLINT NOT NULL ,
		  CLOSING_QUARTER_CODE VARCHAR(20) NOT NULL , 
		  REPORTING_DT VARCHAR(10) , 
		  SCENARIO_ID SMALLINT , 
		  QUOTE_DT VARCHAR(10) NOT NULL , 
		  QUOTE_ID VARCHAR(36) NOT NULL , 
		  MATURITY_DT VARCHAR(10) , 
		  QUOTE_RT DECFLOAT(34) , 
		  RISK_FACTOR_CATEGORY_CD VARCHAR(36) NOT NULL , 
		  ENTITY_ID VARCHAR(36) NOT NULL , 
		  CURRENCY_CD VARCHAR(3) , 
		  RATEINDEX_CT VARCHAR(40),
		  EC_FLAG VARCHAR(5),
		  NORM_CD VARCHAR(7) NOT NULL , 
		  RUN_ID NUMERIC(8) ,
		  INTERVAL VARCHAR(9),
		  ASOFDAY VARCHAR(20) )
		DISTRIBUTE  BY RANDOM IN TBS_<env>  
		 ORGANIZE BY COLUMN    ;
