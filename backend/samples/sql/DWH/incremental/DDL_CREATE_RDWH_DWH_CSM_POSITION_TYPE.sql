DROP TABLE DWH_CSM_POSITION_TYPE IF EXISTS;

CREATE TABLE DWH_CSM_POSITION_TYPE (
   ID                   SMALLINT              
      NOT NULL,
   CODE                 VARCHAR(32)           
      NOT NULL,
   NAME                 VARCHAR(64),
   CREATED_DATE         TIMESTAMP              DEFAULT CURRENT_TIMESTAMP
,
   CONSTRAINT PK_DWH_CSM_POSITION_TYPE PRIMARY KEY (ID)  
)
 ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;

COMMENT ON COLUMN DWH_CSM_POSITION_TYPE.CREATED_DATE IS
'System generated date';