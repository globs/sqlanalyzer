DROP TABLE DWH_LEVEL_OF_ANALYSIS IF EXISTS;

CREATE TABLE DWH_LEVEL_OF_ANALYSIS  (
		  ID INTEGER NOT NULL , 
		  CODE VARCHAR(32) NOT NULL , 
		  NAME VARCHAR(100) NOT NULL , 
		  VALID_FROM TIMESTAMP NOT NULL , 
		  VALID_TO TIMESTAMP NOT NULL , 
		  AFTER_ROLL_FORWARD BOOLEAN , 
		  LOC_CODE_CSM VARCHAR(16) , 
		  IS_AFTER_PERIODICITY BOOLEAN , 
		  SORT_ORDER SMALLINT,   
		  CONSTRAINT PK_DWH_LEVEL_OF_ANALYSIS
          PRIMARY KEY (ID)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;