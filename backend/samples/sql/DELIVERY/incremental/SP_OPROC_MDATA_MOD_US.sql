CREATE OR REPLACE PROCEDURE SP_ORCHESTRATION_PROCESS_METADATA_MOD_US () RETURNS INTEGER
   LANGUAGE nzplsql AS BEGIN_PROC

DECLARE VAR_ORCHESTRATION_PROCESS_ID INT ;
VAR_ORCHESTRATION_PROCESS_STEP_ID INT ;
VAR_ORCHESTRATION_PROCESS_STEP1_ID INT ;
VAR_BATCH_GROUPING_ID INT ;
VAR_BATCH_GROUPING_ID_02 INT;
VAR_BATCH_ID INT;
VAR_BATCH_INSTANCE_ID INT;
VAR_JOB_GROUP_ID INT;
VAR_JOB_ID INT;
VAR_JOB_ID_01 INT;
VAR_STEP INT;
VAR_DATA_VALIDATION_CONTROL_ID INT;
VAR_DATA_VALIDATION_CONTROL_MAPPING_ID INT;
L_ERR_CD				CHAR(5);
L_ERR_MSG				VARCHAR(32000); 


BEGIN
SET ISOLATION TO UR;

SET VAR_ORCHESTRATION_PROCESS_ID  = NEXT VALUE FOR SEQUENCE_ORCHESTRATION_PROCESS_METADATA;
SET VAR_ORCHESTRATION_PROCESS_STEP_ID  = NEXT VALUE FOR SEQUENCE_ORCHESTRATION_PROCESS_METADATA;
SET VAR_BATCH_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_BATCH_INSTANCE_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_JOB_GROUP_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_JOB_ID  = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_STEP = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;

INSERT INTO ORCHESTRATION_PROCESS (ORCHESTRATION_PROCESS_ID,ORCHESTRATION_PROCESS_NAME,ORCHESTRATION_PROCESS_DESCRIPTION,INSTANCE_TYPE)
	VALUES (VAR_ORCHESTRATION_PROCESS_ID,'US Modeling Engine to DIP','US Modeling Engine to DIP', null);

INSERT INTO ORCHESTRATION_PROCESS_STEP (ORCHESTRATION_PROCESS_STEP_ID,ORCHESTRATION_PROCESS_ID,ORCHESTRATION_PROCESS_STEP_NAME
,CHILD_BATCH_ID,TRIGGER_DEPENDENT_STEPS, STARTING_STEP, OPTIONAL_RUN)
	VALUES (VAR_ORCHESTRATION_PROCESS_STEP_ID, VAR_ORCHESTRATION_PROCESS_ID,'Process US CF in Staging',VAR_BATCH_ID,NULL, true, false);


INSERT INTO BATCH (BATCH_ID,BATCH_NAME,BATCH_DESCRIPTION,APPLICATION_NAME,BATCH_PRIORITY,ON_ERROR,PRE_BATCH_PROCESSING,POST_BATCH_PROCESSING) VALUES
(VAR_BATCH_ID,'Talend US Staging Process','Talend US Staging Process','TALEND', 1,NULL,NULL,'[POST-COMPLETION]api-dip:checkIpdsAutoTrigger');

INSERT INTO JOB_GROUP (JOB_GROUP_ID,JOB_GROUP_NAME,BATCH_ID,PARENT_JOB_GROUP_ID) VALUES
(VAR_JOB_GROUP_ID,'Talend US Staging Process Job Group',VAR_BATCH_ID,NULL);

INSERT INTO JOB ( JOB_ID, JOB_NAME, JOB_PRIORITY, JOB_GROUP_ID, IS_ACTIVE, JOB_OPTIONAL_RUN )
        VALUES ( VAR_JOB_ID, 'Talend US Staging Process', NULL, VAR_JOB_GROUP_ID, NULL, NULL );
 

INSERT INTO STEP ( STEP_ID, STEP_TYPE_ID, JOB_ID, STEP_NAME, STEP_SEQUENCE ,IS_ACTIVE, PRE_STEP_PROCESSING,STEP_PROCESSING, POST_STEP_PROCESSING)
        VALUES ( VAR_STEP, 2, VAR_JOB_ID, 'Staging Load Step ', 1, NULL, 'api-dip:uploadinsert','api-diptalend:us_cashflow_staging_load', NULL);
 
INSERT INTO STAGING_LOAD_STEP ( STEP_ID, STAGING_SCHEMA_NAME, STAGING_TABLE_NAME, TRUNCATE_STAGING_TABLE, MANDATORY_FILE, FILE_HEADER_ROWS
, DELIMITER, CONVERTED_STAGING_TABLE_NAME, CHECK_DUPLICATES, SPECIFIC_PROCESS, DATA_FILE_TREATMENT, REGIONAL_DATA, FILE_DIRECTORY, FILE_NAME_PATTERN ) 
        VALUES ( VAR_STEP, 'STAGING_' || chr(36) || '{ENV}', 'STAGING_US_PROJECTION', 0, 0, 0, '~', NULL, 0, NULL, NULL, 0, NULL, NULL );

SELECT DATA_VALIDATION_CONTROL_ID INTO VAR_DATA_VALIDATION_CONTROL_ID FROM DATA_VALIDATION_CONTROL WHERE CONTROL_NAME = 'Staging Number of Rows Control';

IF VAR_DATA_VALIDATION_CONTROL_ID IS NULL THEN 
	SET VAR_DATA_VALIDATION_CONTROL_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;

	INSERT INTO DATA_VALIDATION_CONTROL (DATA_VALIDATION_CONTROL_ID, CONTROL_NAME, CONTROL_PROCESSING, SPECIFIC_PROCESS) 
		VALUES(VAR_DATA_VALIDATION_CONTROL_ID, 'Staging Number of Rows Control', 'api-dip:stagingNumRowsControl', NULL);
END IF;


SET VAR_DATA_VALIDATION_CONTROL_MAPPING_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;

INSERT INTO DATA_VALIDATION_CONTROL_MAPPING (DATA_VALIDATION_CONTROL_MAPPING_ID, BATCH_ID, JOB_GROUP_ID, JOB_ID, STEP_ID
, DATA_VALIDATION_CONTROL_ID, IS_ACTIVE, CONTROL_TYPE, CONTROL_CHRONOLOGY, CONTROL_SEQUENCE, PARAMETER_NAMES) 
	VALUES(VAR_DATA_VALIDATION_CONTROL_MAPPING_ID, VAR_BATCH_ID, VAR_JOB_GROUP_ID, VAR_JOB_ID, VAR_STEP
	, VAR_DATA_VALIDATION_CONTROL_ID, TRUE, 'INFO', 'POST-POST', 1, NULL);

SET VAR_BATCH_GROUPING_ID = NEXT VALUE FOR SEQUENCE_BATCH_GROUPING;

INSERT INTO BATCH_GROUPING (BATCH_GROUPING_ID, BATCH_GROUPING_NAME, ACTIVE_DWH, PASSIVE_DWH, LAST_UPDATE_TIME)
		VALUES(VAR_BATCH_GROUPING_ID, 'US RDWH STAGING PROCESSING', 'DWHD2_' || chr(36) || '{ENV}', 'DWHD1_' || chr(36) || '{ENV}', CURRENT_TIMESTAMP);

INSERT INTO EVENT ( EVENT_CODE, EVENT_DESCRIPTION )
        VALUES ( 'EVT_INGEST_US_CASHFLOWS_FOR_VALIDATION', 'Event for Staging Load' );

INSERT INTO EVENT_MAPPING ( EVENT_CODE, BATCH_ID, ORCHESTRATION_PROCESS_STEP_ID, ORCHESTRATION_PROCESS_ID, BATCH_GROUPING_ID, IS_DWH_SWITCH)
        VALUES ( 'EVT_INGEST_US_CASHFLOWS_FOR_VALIDATION', VAR_BATCH_ID, VAR_ORCHESTRATION_PROCESS_STEP_ID, VAR_ORCHESTRATION_PROCESS_ID
		, VAR_BATCH_GROUPING_ID, FALSE);



SET VAR_ORCHESTRATION_PROCESS_STEP1_ID = NEXT VALUE FOR SEQUENCE_ORCHESTRATION_PROCESS_METADATA;
SET VAR_BATCH_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_BATCH_INSTANCE_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_JOB_GROUP_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;
SET VAR_JOB_ID = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;

INSERT INTO ORCHESTRATION_PROCESS_STEP (ORCHESTRATION_PROCESS_STEP_ID,ORCHESTRATION_PROCESS_ID,ORCHESTRATION_PROCESS_STEP_NAME
,CHILD_BATCH_ID,TRIGGER_DEPENDENT_STEPS, STARTING_STEP, OPTIONAL_RUN)
	VALUES (VAR_ORCHESTRATION_PROCESS_STEP1_ID, VAR_ORCHESTRATION_PROCESS_ID,'Process US CF in RDWH', VAR_BATCH_ID, NULL, false, false);

INSERT INTO ORCHESTRATION_PROCESS_STEP_DEPENDENCY(ORCHESTRATION_PROCESS_STEP_ID, PARENT_ORCHESTRATION_PROCESS_STEP_ID)
	VALUES (VAR_ORCHESTRATION_PROCESS_STEP1_ID, VAR_ORCHESTRATION_PROCESS_STEP_ID);


INSERT INTO BATCH (BATCH_ID,BATCH_NAME,BATCH_DESCRIPTION,APPLICATION_NAME,BATCH_PRIORITY,ON_ERROR,PRE_BATCH_PROCESSING,POST_BATCH_PROCESSING)
	VALUES (VAR_BATCH_ID,'Talend US DWH Process','Talend US DWH Process','TALEND', 1,NULL,NULL,'api-dip:postIpdsProcessing');

INSERT INTO JOB_GROUP (JOB_GROUP_ID,JOB_GROUP_NAME,BATCH_ID,PARENT_JOB_GROUP_ID)
	VALUES (VAR_JOB_GROUP_ID,'Talend US DWH Process Job Group',VAR_BATCH_ID,NULL);

INSERT INTO JOB ( JOB_ID, JOB_NAME, JOB_PRIORITY, JOB_GROUP_ID, IS_ACTIVE, JOB_OPTIONAL_RUN )
	VALUES ( VAR_JOB_ID, 'Talend US DWH Process', NULL, VAR_JOB_GROUP_ID, NULL, NULL);


SET VAR_STEP = NEXT VALUE FOR SEQUENCE_BATCH_METADATA;

INSERT INTO STEP ( STEP_ID, STEP_TYPE_ID, JOB_ID, STEP_NAME, STEP_SEQUENCE ,IS_ACTIVE, PRE_STEP_PROCESSING,STEP_PROCESSING, POST_STEP_PROCESSING)
        VALUES ( VAR_STEP, 3, VAR_JOB_ID, 'INSERT US CF in RDWH', 1, NULL, 'api-dip:uploadupdate','api-diptalend:us_cashflow_dwh_load',NULL);
 
INSERT INTO DWH_LOAD_STEP ( STEP_ID, DWH_SCHEMA_NAME, DWH_TABLE_NAME, STAGING_SCHEMA_NAME, STAGING_TABLE_NAME, INGESTION_MODE
, HISTORIZATION_TYPE, SPECIFIC_PROCESS ) 
        VALUES ( VAR_STEP, NULL, NULL, NULL, NULL, NULL, NULL, NULL );

SELECT BATCH_GROUPING_ID INTO VAR_BATCH_GROUPING_ID_02 FROM BATCH_GROUPING WHERE BATCH_GROUPING_NAME = 'RDWH HISTORIZATION PROCESSING';

IF VAR_BATCH_GROUPING_ID_02 IS NULL THEN 
	SET VAR_BATCH_GROUPING_ID_02 = NEXT VALUE FOR SEQUENCE_BATCH_GROUPING;

	INSERT INTO BATCH_GROUPING (BATCH_GROUPING_ID, BATCH_GROUPING_NAME, ACTIVE_DWH, PASSIVE_DWH, LAST_UPDATE_TIME)
		VALUES(VAR_BATCH_GROUPING_ID_02, 'RDWH HISTORIZATION PROCESSING', 'DWHD2_' || chr(36) || '{ENV}', 'DWHD1_' || chr(36) || '{ENV}', CURRENT_TIMESTAMP);

END IF;

INSERT INTO EVENT ( EVENT_CODE, EVENT_DESCRIPTION )
        VALUES ( 'EVT_US_CASHFLOWS_VALIDATED', 'Event to Process CF in RDWH' );

INSERT INTO EVENT_MAPPING ( EVENT_CODE, BATCH_ID, ORCHESTRATION_PROCESS_STEP_ID, ORCHESTRATION_PROCESS_ID, BATCH_GROUPING_ID, IS_DWH_SWITCH)
        VALUES ( 'EVT_US_CASHFLOWS_VALIDATED', VAR_BATCH_ID, VAR_ORCHESTRATION_PROCESS_STEP1_ID, NULL, VAR_BATCH_GROUPING_ID_02, TRUE);
		
EXCEPTION WHEN OTHERS THEN 
	L_ERR_CD := SUBSTR(SQLERRM, 8, 5); 
	L_ERR_MSG := SQLERRM; 
	RAISE EXCEPTION '% Error while executing SQL statement', L_ERR_MSG; 
	RETURN L_ERR_CD; 
 
END; 

END_PROC;