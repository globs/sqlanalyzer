DROP TABLE BUSINESS_EVENT IF EXISTS;
CREATE TABLE BUSINESS_EVENT
(
	BUSINESS_EVENT_ID	                    INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	EVENT_CODE		                        VARCHAR(128) NOT NULL,
	CLOSING_TYPE_CODE						VARCHAR(32),
	SCENARIO_TYPE							VARCHAR(32),
	RUN_TYPE								VARCHAR(32),
	IS_ACTIVE								BOOLEAN,
	TRIGGER_MODE						    VARCHAR(128),
	DELAY_DAYS                              VARCHAR(32) 
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE BUSINESS_EVENT ADD CONSTRAINT BUSINESS_EVENT_PK PRIMARY KEY (BUSINESS_EVENT_ID);