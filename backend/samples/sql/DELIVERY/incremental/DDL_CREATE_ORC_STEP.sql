DROP TABLE STEP IF EXISTS;
CREATE TABLE STEP
(
	STEP_ID	                  INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	STEP_TYPE_ID	          SMALLINT NOT NULL,
	JOB_ID	                  INT NOT NULL,
	STEP_NAME	              VARCHAR(128) NOT NULL,
	STEP_SEQUENCE	          SMALLINT NOT NULL,
	IS_ACTIVE				  BOOLEAN,
	PRE_STEP_PROCESSING	      VARCHAR(256),
	STEP_PROCESSING	          VARCHAR(256),
	POST_STEP_PROCESSING	  VARCHAR(256)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE STEP ADD CONSTRAINT STEP_PK PRIMARY KEY (STEP_ID);
