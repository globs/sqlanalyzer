DROP TABLE APPLICATION_CONTROL IF EXISTS;

CREATE TABLE APPLICATION_CONTROL
(
    JOBSTREAM_ID            BIGINT       GENERATED BY DEFAULT AS IDENTITY ( START WITH 1, INCREMENT BY 1, CYCLE, MINVALUE 1, MAXVALUE 9223372036854775807, CACHE 5000, NO ORDER ),
    START_TIME              TIMESTAMP(6) NOT NULL,
    END_TIME                TIMESTAMP(6),
    APPLICATION             VARCHAR(100) NOT NULL,
    COMPLETION_FLAG         CHARACTER(1) NOT NULL DEFAULT 'N',
    STATUS                  VARCHAR(30),
    BUILD_NAME              VARCHAR(100)
) 
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;