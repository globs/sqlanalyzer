DROP TABLE JOB_EXECUTION IF EXISTS;
CREATE TABLE JOB_EXECUTION
(
	JOB_EXECUTION_ID	         INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	JOB_GROUP_EXECUTION_ID	     INT NOT NULL,
	JOB_ID	                     INT NOT NULL,
	JOB_EXECUTION_PARAMS		 VARCHAR(5000),
	CREATE_TIME	                 TIMESTAMP NOT NULL,
	START_TIME	                 TIMESTAMP,
	END_TIME	                 TIMESTAMP,
	STATUS	                     VARCHAR(16),
	EXIT_CODE	                 VARCHAR(16),
	EXIT_MESSAGE	             VARCHAR(10000)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE JOB_EXECUTION ADD CONSTRAINT JOB_EXECUTION_PK PRIMARY KEY (JOB_EXECUTION_ID);