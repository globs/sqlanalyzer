DROP TABLE BUSINESS_PROCESS IF EXISTS;
CREATE TABLE BUSINESS_PROCESS
(
	BUSINESS_PROCESS_ID	                    INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	BUSINESS_PROCESS_NAME                   VARCHAR(128) NOT NULL,
	CLOSING_TYPE_CODE						VARCHAR(32),
	SCENARIO_TYPE							VARCHAR(32),
	RUN_TYPE								VARCHAR(32)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE BUSINESS_PROCESS ADD CONSTRAINT BUSINESS_PROCESS_PK PRIMARY KEY (BUSINESS_PROCESS_ID);