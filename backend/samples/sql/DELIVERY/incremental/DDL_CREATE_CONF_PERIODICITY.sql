DROP TABLE CONF_PERIODICITY IF EXISTS;

CREATE TABLE CONF_PERIODICITY  (
	"PERIODICITY_TYPE"      SMALLINT NOT NULL , 
	"PERIOD_TYPE_ID"        SMALLINT NOT NULL , 
	"MIN_INTERVAL"          SMALLINT , 
	"MAX_INTERVAL"          SMALLINT , 
	"IS_ACTIVE"             BOOLEAN , 
	"MIN_CLOSING_DATE"      DATE NOT NULL , 
	"MAX_CLOSING_DATE"      DATE NOT NULL,
CONSTRAINT CONF_PERIODICITY PRIMARY KEY (PERIODICITY_TYPE,MIN_CLOSING_DATE,MAX_CLOSING_DATE) 
) DISTRIBUTE BY RANDOM IN TBS_<env>;  
