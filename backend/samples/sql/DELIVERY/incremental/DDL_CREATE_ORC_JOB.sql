DROP TABLE JOB IF EXISTS;
CREATE TABLE JOB
(
	JOB_ID	            INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	JOB_NAME	        VARCHAR(128) NOT NULL,
	JOB_PRIORITY	    SMALLINT,
	JOB_GROUP_ID	    INT NOT NULL,
	IS_ACTIVE			BOOLEAN,
	JOB_OPTIONAL_RUN	BOOLEAN,
	JOB_PARAMS			VARCHAR(5000),
	INSTANCE_TYPE		VARCHAR(32),
	EXECUTION_TYPE		VARCHAR(32)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE JOB ADD CONSTRAINT JOB_PK PRIMARY KEY (JOB_ID);
