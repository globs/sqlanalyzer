DROP TABLE BATCH IF EXISTS;
CREATE TABLE BATCH
(
	BATCH_ID	                          INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	BATCH_NAME	                          VARCHAR(128) NOT NULL,
	BATCH_DESCRIPTION	                  VARCHAR(1024),
	APPLICATION_NAME	                  VARCHAR(128) NOT NULL,
	BATCH_PRIORITY	                      SMALLINT,
	ON_ERROR	                          VARCHAR(128),
	PRE_BATCH_PROCESSING                  VARCHAR(256),
	POST_BATCH_PROCESSING                 VARCHAR(256)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE BATCH ADD CONSTRAINT BATCH_PK PRIMARY KEY (BATCH_ID);