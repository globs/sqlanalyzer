DROP TABLE BATCH_EXECUTION IF EXISTS;
CREATE TABLE BATCH_EXECUTION
(
	BATCH_EXECUTION_ID	                     INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	BATCH_INSTANCE_ID	                     INT NOT NULL,
	EVENT_INSTANCE_ID	                     INT,
	CREATE_TIME	                             TIMESTAMP NOT NULL,
	START_TIME	                             TIMESTAMP,
	END_TIME	                             TIMESTAMP,
	STATUS	                                 VARCHAR(16),
	EXIT_CODE	                             VARCHAR(16),
	EXIT_MESSAGE	                         VARCHAR(10000)
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE BATCH_EXECUTION ADD CONSTRAINT BATCH_EXECUTION_PK PRIMARY KEY (BATCH_EXECUTION_ID);