DROP TABLE STEP_EXECUTION IF EXISTS;
CREATE TABLE STEP_EXECUTION
(
	STEP_EXECUTION_ID	    INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	JOB_EXECUTION_ID	    INT NOT NULL,
	STEP_ID	                INT NOT NULL,
	CREATE_TIME	            TIMESTAMP NOT NULL,
	START_TIME	            TIMESTAMP,
	END_TIME	            TIMESTAMP,
	STATUS	                VARCHAR(16),
	QUERY	                VARCHAR(10000),
	FULL_FILE_NAME	        VARCHAR(256),
	EXIT_CODE	            VARCHAR(16),
	EXIT_MESSAGE	        VARCHAR(10000),
	NUM_ROWS_PROCESSED      INT
) ORGANIZE BY ROW DISTRIBUTE ON RANDOM IN TBS_<env>;
ALTER TABLE STEP_EXECUTION ADD CONSTRAINT STEP_EXECUTION_PK PRIMARY KEY (STEP_EXECUTION_ID);