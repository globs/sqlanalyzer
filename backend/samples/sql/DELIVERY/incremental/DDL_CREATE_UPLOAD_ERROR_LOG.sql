DROP TABLE UPLOAD_ERROR_LOG IF EXISTS;

CREATE TABLE UPLOAD_ERROR_LOG
(
    ERROR_ID         BIGINT  GENERATED ALWAYS AS IDENTITY ( START WITH 1, INCREMENT BY 1, NO CYCLE, MINVALUE 1, MAXVALUE 9223372036854775807, CACHE 20, NO ORDER ),
    REQUEST_ID       BIGINT  NOT NULL,
    ERROR_MESSAGE_ID INTEGER NOT NULL,
    ERROR_ROW        INTEGER,
    ERROR_COLUMN     INTEGER,
    CONSTRAINT PK_UPLOAD_ERROR_LOG
    PRIMARY KEY (ERROR_ID)
)  
ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;