SET SCHEMA SHARE_<env>;

CREATE OR REPLACE VIEW V_IFRS17_SUB_PORTFOLIO AS 
SELECT 
CAST("IFRS17_Sub_Portfolio" AS CHAR(10)) AS "IFRS17_Sub_Portfolio" ,
CAST("IFRS17_Sub_Portfolio_LAB" AS VARCHAR(64)) AS "IFRS17_Sub_Portfolio_LAB"
FROM
(SELECT 
GRPIFRSSEG1_CT AS "IFRS17_Sub_Portfolio",
GRPIFRSSEG1_LL AS "IFRS17_Sub_Portfolio_LAB"
FROM BI_<env>.TSECIFRS
GROUP BY 
GRPIFRSSEG1_CT,
GRPIFRSSEG1_LL);