SET SCHEMA SHARE_<env>;

CREATE OR REPLACE
VIEW V_CONTRACT_STATUS AS
SELECT
    CAST(COLVAL_CT AS CHAR(5)) AS "Contract_Status",
    CAST(COLVAL_LS AS VARCHAR(16)) AS "Contract_Status_Description"
FROM
    BI_<env>.TBANTECL
WHERE
    COL_LS = 'CTRSTS_CT'
    AND LAG_CF = 'E'
GROUP BY
    COLVAL_CT,
    COLVAL_LS;