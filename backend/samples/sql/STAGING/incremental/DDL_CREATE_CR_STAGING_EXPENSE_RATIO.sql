SET SCHEMA STAGING_<env>;

DROP TABLE STAGING_EXPENSE_RATIO IF EXISTS;

CREATE TABLE STAGING_EXPENSE_RATIO
(
    
    LINENUMBER          VARCHAR(255),
	CONTRACT_CATEGORY   VARCHAR(255),
	RATIO_TYPE          VARCHAR(255),
	SEGMENT_LLOYDS      VARCHAR(255),
	SUBSIDIARY          VARCHAR(255),
	SUBLEDGER           VARCHAR(255),
	MARKET              VARCHAR(255),
	PORTFOLIO           VARCHAR(255),
	NATURE              VARCHAR(255),
	CURRENCY            VARCHAR(255),
	CURRENCY_TYPE       VARCHAR(255),
	EXPENSES            VARCHAR(255),
	DRIVER              VARCHAR(255),
	EXPENSE_RATIO       VARCHAR(255),
	COHORT              VARCHAR(255),
	PCA_AMOUNT          VARCHAR(255),
	ALLOCATION_RATIO    VARCHAR(255)
)
ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;