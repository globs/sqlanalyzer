SET SCHEMA STAGING_<env>;

DROP TABLE WRK_FX_RATE IF EXISTS;

CREATE TABLE WRK_FX_RATE (
	"Currency_From" CHAR(3) NOT NULL,
	"Currency_To" CHAR(3) NOT NULL,
	"Modified_Date" DATE NOT NULL,
	"As_Of_Date" DATE NOT NULL,
	"Name" VARCHAR(16) NOT NULL,
	"Type_Of_Rate" CHAR(1) NOT NULL,
	"Reference_Valid_From" DATE NOT NULL,
	"Exchange_Rate" DECIMAL(15,8) NOT NULL
)
ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;
