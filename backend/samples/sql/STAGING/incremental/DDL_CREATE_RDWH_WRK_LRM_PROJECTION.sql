SET SCHEMA STAGING_<env>;

DROP TABLE WRK_LRM_PROJECTION IF EXISTS;

CREATE TABLE WRK_LRM_PROJECTION
(
    CONTRACT_NUMBER         CHARACTER(9)  NOT NULL,
    SECTION_NUMBER          INTEGER,
    POLICY_UWY              INTEGER,
    REPORTING_BASIS_CODE    VARCHAR(100)  NOT NULL,
    BUSINESS_MATURITY_CODE  VARCHAR(100)  NOT NULL,
    CLOSING_DATE            DATE          NOT NULL,
    ASSUMED_CONTRACT_NUMBER CHARACTER(9),
    ASSUMED_SECTION_NUMBER  INTEGER,
    CURRENCY_CODE           VARCHAR(3)    NOT NULL,
    POSITION_CODE           VARCHAR(100)  NOT NULL,
    SCENARIO_TYPE_CODE      VARCHAR(100)  NOT NULL,
    SCENARIO_PARAMETER      DECIMAL(10,6) NOT NULL,
    LEVEL_OF_ANALYSIS_CODE  VARCHAR(100)  NOT NULL,
    PERIOD_DESCRIPTION      VARCHAR(10)   NOT NULL,
    PROJECTION_MONTH        INTEGER       NOT NULL,
    PROJECTION_YEAR         INTEGER       NOT NULL,
    AMOUNT                  DECFLOAT(16)  NOT NULL,
    CREATED_BY              VARCHAR(20)   NOT NULL,
    CREATED_DATE            TIMESTAMP(6)  NOT NULL,
    LINE_NUMBER             BIGINT        NOT NULL,
    REQUEST_ID              BIGINT        NOT NULL,
    HISTORIZATION_MODE      VARCHAR(64)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;