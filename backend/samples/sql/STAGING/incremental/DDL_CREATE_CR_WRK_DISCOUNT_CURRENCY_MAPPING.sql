SET SCHEMA STAGING_<env>;

DROP TABLE WRK_DISCOUNT_CURRENCY_MAPPING IF EXISTS;

CREATE TABLE WRK_DISCOUNT_CURRENCY_MAPPING (
	CURRENCY_CODE			CHAR(3)	NOT NULL,
	DISCOUNT_CURRENCY_CODE	CHAR(3)	NOT NULL,
	LOB_CODE				CHAR(2),
	REQUEST_ID				BIGINT,
	CONSTRAINT PK_DISCOUNT_CURRENCY PRIMARY KEY (CURRENCY_CODE, DISCOUNT_CURRENCY_CODE)
)
IN TBS_<env> DISTRIBUTE ON RANDOM;