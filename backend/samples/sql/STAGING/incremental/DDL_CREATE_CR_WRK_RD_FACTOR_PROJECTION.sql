SET SCHEMA STAGING_<env>;

DROP TABLE WRK_RD_FACTOR_PROJECTION IF EXISTS;

CREATE TABLE WRK_RD_FACTOR_PROJECTION (
   CONTRACT_NUMBER      CHAR(9)           NOT NULL,
   SECTION_NUMBER       SMALLINT          NOT NULL,
   UNDERWRITING_YEAR    SMALLINT          NOT NULL DEFAULT 0,
   CALCULATED_POSITION_ID INTEGER         NOT NULL,
   LEVEL_OF_ANALYSIS_ID INTEGER           NOT NULL,
   REPORTING_BASIS_ID   SMALLINT,
   CLOSING_DATE         DATE              NOT NULL,
   ASSUMED_CONTRACT_NUMBER CHAR(9),
   ASSUMED_SECTION_NUMBER SMALLINT,
   PROJECTION_YEAR      SMALLINT          NOT NULL,
   PROJECTION_PERIOD    SMALLINT          NOT NULL,
   PERIOD_TYPE_ID       SMALLINT,
   IS_PRESENT_VALUE     BOOLEAN,
   FACTOR               DECFLOAT         NOT NULL,
   REQUEST_ID           BIGINT           NOT NULL
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;
