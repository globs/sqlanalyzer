SET SCHEMA STAGING_<env>;

DROP TABLE DWH_EXPENSE_RATIO_PLAN IF EXISTS;

CREATE TABLE DWH_EXPENSE_RATIO_PLAN
(
    VERSION_YEAR            SMALLINT,
    VERSION_QUARTER         VARCHAR(16),
    VERSION_TYPE            VARCHAR(16),
    PRODUCT_LINE            VARCHAR(16),
    SEGMENT_CODE            SMALLINT,
    SEGMENT_LABEL           VARCHAR(64),
    EXPENSE_RATIO_TYPE      VARCHAR(16),
    CURRENCY_CODE           CHARACTER(3),
    ALLOCATED_AMOUNT        DECFLOAT,
    EXPENSE_RATIO           DECFLOAT
)
   ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;