SET SCHEMA STAGING_<env>;

DROP TABLE STAGING_YIELD_CURVE IF EXISTS;

CREATE TABLE STAGING_YIELD_CURVE
(
	LINE_NUMBER		VARCHAR(255) , 
	CURRENCY_CODE 	VARCHAR(255) , 
	NORM_CODE 		VARCHAR(255) , 
	SEGMENT_CODE 	VARCHAR(255) , 
	CURVE_TYPE 		VARCHAR(255) , 
	MATURITY_TYPE 	VARCHAR(255) , 
	MATURITY 		VARCHAR(255) , 
	RATE 			VARCHAR(255) , 
	ID 				VARCHAR(255)
)   
	ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;
		 
