SET SCHEMA STAGING_<env>;

DROP TABLE STAGING_RISK_ADJUSTMENT IF EXISTS;

CREATE TABLE STAGING_RISK_ADJUSTMENT
(    
    LINE_NUMBER             VARCHAR(255),
    SUBSIDIARY              VARCHAR(255),
    SUBLEDGER               VARCHAR(255),
    LOB_N1                  VARCHAR(255),
    NATURE                  VARCHAR(255),
    NORM                    VARCHAR(255),
    DOMAIN_CODE             VARCHAR(255),
    PREMIUM                 VARCHAR(255),
    RESERVES                VARCHAR(255)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;