CREATE TABLE WRK_YC_LIFE
(    
    SRC_PARAMETER_ID        BIGINT,
    TGT_PARAMETER_ID        BIGINT,
    PARAMETER_SET_ID        BIGINT,
    PARAMETER_TYPE_ID       SMALLINT,
    SEGMENT_CODE            VARCHAR(16),
    SRC_CURRENCY_CODE       CHARACTER(3),
    SRC_MATURITY            INTEGER,
