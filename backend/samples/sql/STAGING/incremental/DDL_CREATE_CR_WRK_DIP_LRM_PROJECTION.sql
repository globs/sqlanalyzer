SET SCHEMA STAGING_<env>;

DROP TABLE WRK_DIP_LRM_PROJECTION IF EXISTS;

CREATE TABLE WRK_DIP_LRM_PROJECTION (
	ID 						BIGINT NOT NULL,
	CURRENCY_CODE 			CHAR(3) NOT NULL,
	MAP_POSITION_CODE 		VARCHAR(32),
	SCENARIO_TYPE_CODE 		VARCHAR(32) NOT NULL,
	SCENARIO_PARAMETER 		DECIMAL(10,6) NOT NULL,
	LEVEL_OF_ANALYSIS_CODE 	VARCHAR(32) NOT NULL,
	CLOSING_DATE 			DATE NOT NULL,	
	PERIOD 					VARCHAR(18),
	AMOUNT 					DECFLOAT NOT NULL
)
ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;