SET SCHEMA STAGING_<env>;

DROP TABLE WRK_YIELD_CURVE IF EXISTS;

CREATE TABLE WRK_YIELD_CURVE
(
    CURRENCY_CODE           CHARACTER(3) NOT NULL,
    NORM_CODE               VARCHAR(16)  NOT NULL,
    SEGMENT_CODE            VARCHAR(16),
    CURVE_TYPE              VARCHAR(16)  NOT NULL,
    MATURITY_TYPE           VARCHAR(1),
    MATURITY                INTEGER,
    RATE                    DECFLOAT,
    ID                      VARCHAR(32)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;