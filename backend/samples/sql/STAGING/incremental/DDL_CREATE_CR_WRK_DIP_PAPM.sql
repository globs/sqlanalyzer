SET SCHEMA STAGING_<env>;

DROP TABLE WRK_DIP_PAPM IF EXISTS;

CREATE TABLE WRK_DIP_PAPM
(    
    YEAR            INTEGER,
    SUBSIDIARY      SMALLINT        NOT NULL,
    SUBLEDGER       SMALLINT        NOT NULL,
    MARKET          SMALLINT,
    SEGMENT         VARCHAR(1)      NOT NULL,
    NATURE          CHARACTER(1),
    CURRENCY        VARCHAR(3)      NOT NULL,
    CLAIMSAMOUNT    DECIMAL(31,2)
)
    ORGANIZE BY COLUMN IN TBS_<env> DISTRIBUTE ON RANDOM;