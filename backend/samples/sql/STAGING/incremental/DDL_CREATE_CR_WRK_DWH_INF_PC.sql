SET SCHEMA STAGING_<env>;

DROP TABLE WRK_DWH_INF_PC IF EXISTS;

CREATE TABLE WRK_DWH_INF_PC
(
    
    PARAMETER_SET_ID        BIGINT,
    CURRENCY_CODE           CHARACTER(3),
