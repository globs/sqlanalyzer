SET SCHEMA STAGING_<env>;

DROP TABLE WRK_PROJECTION_ALLOCATION IF EXISTS;

CREATE TABLE WRK_PROJECTION_ALLOCATION (
   CONTRACT_NUMBER      CHAR(9)         NOT NULL,
   SECTION_NUMBER       SMALLINT        NOT NULL,
   ASSUMED_CONTRACT_NUMBER CHAR(9),
   ASSUMED_SECTION_NUMBER SMALLINT,
   ALLOCATION_STATUS_ID BIGINT,
   REPORTING_BASIS_ID   SMALLINT,
   CLOSING_DATE         DATE,
   LEVEL_OF_ANALYSIS_ID INTEGER,
   REQUEST_ID           BIGINT
)
IN TBS_<env> DISTRIBUTE ON RANDOM;